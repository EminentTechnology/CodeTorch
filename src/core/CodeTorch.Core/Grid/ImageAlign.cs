using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CodeTorch.Core
{
    public enum ImageAlign
    {
        //
        // Summary:
        //     The alignment is not set.
        NotSet = 0,
        //
        // Summary:
        //     The image is aligned on the left edge of the Web page with text wrapping on the
        //     right.
        Left = 1,
        //
        // Summary:
        //     The image is aligned on the right edge of the Web page with text wrapping on
        //     the left.
        Right = 2,
        //
        // Summary:
        //     The lower edge of the image is aligned with the lower edge of the first line
        //     of text.
        Baseline = 3,
        //
        // Summary:
        //     The upper edge of the image is aligned with the upper edge of the highest element
        //     on the same line.
        Top = 4,
        //
        // Summary:
        //     The middle of the image is aligned with the lower edge of the first line of text.
        Middle = 5,
        //
        // Summary:
        //     The lower edge of the image is aligned with the lower edge of the first line
        //     of text.
        Bottom = 6,
        //
        // Summary:
        //     The lower edge of the image is aligned with the lower edge of the largest element
        //     on the same line.
        AbsBottom = 7,
        //
        // Summary:
        //     The middle of the image is aligned with the middle of the largest element on
        //     the same line.
        AbsMiddle = 8,
        //
        // Summary:
        //     The upper edge of the image is aligned with the upper edge of the highest text
        //     on the same line.
        TextTop = 9
    }
}
